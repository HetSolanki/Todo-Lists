<?php 

    # -- abs() -- #
    echo abs(-12) . "\n";

    # -- ceil() -- #
    echo ceil(-12.2) . "\n";

    # -- floor() -- #
    echo floor(12.2) . "\n";

    # -- round() -- #
    echo round(12.455, 2, PHP_ROUND_HALF_ODD) . "\n";

    # -- min() -- #
    echo min(12, 123, 34, 10) . "\n";

    # -- max() -- #
    echo max(12, 123, 34, 10) . "\n";

    # -- pow() -- #
    echo pow(-12, 1) . "\n";

    # -- sqrt() -- #
    echo sqrt(25) . "\n";
?>